function dedupeEmojis(list: readonly string[]): string[] {
  const out: string[] = [];
  const seen = new Set<string>();
  for (const s of list) {
    if (!s || seen.has(s)) continue;
    seen.add(s);
    out.push(s);
  }
  return out;
}

type EmojiGridTab = Exclude<TableChatPickerTabId, 'phrases' | 'mine'>;

export function getEmojiCellsForTab(tab: EmojiGridTab, expanded: boolean): string[] {
  const packs: Record<EmojiGridTab, readonly [readonly string[], readonly string[]]> = {
    react: [REACTION_EMOJI_PRIMARY, REACTION_EMOJI_MORE],
    cards: [CARDS_EMOJI_PRIMARY, CARDS_EMOJI_MORE],
    misc: [MISC_EMOJI_PRIMARY, MISC_EMOJI_MORE],
  };
  const [primary, more] = packs[tab];
  return expanded ? dedupeEmojis([...primary, ...more]) : dedupeEmojis([...primary]);
}

export function tabHasEmojiMore(tab: EmojiGridTab): boolean {
  const packs: Record<EmojiGridTab, readonly [readonly string[], readonly string[]]> = {
    react: [REACTION_EMOJI_PRIMARY, REACTION_EMOJI_MORE],
    cards: [CARDS_EMOJI_PRIMARY, CARDS_EMOJI_MORE],
    misc: [MISC_EMOJI_PRIMARY, MISC_EMOJI_MORE],
  };
  const [, more] = packs[tab];
  return more.length > 0;
}
